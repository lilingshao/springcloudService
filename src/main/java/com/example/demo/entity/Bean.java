package com.example.demo.entity;

public class Bean {
	private String name;
	private String likes;
	private String id;
 
	public String getName() {
		return name;
	}
 
	public void setName(String name) {
		this.name = name;
	}
 
	public String getLikes() {
		return likes;
	}
 
	public void setLikes(String likes) {
		this.likes = likes;
	}
 
	public String getId() {
		return id;
	}
 
	public void setId(String id) {
		this.id = id;
	}
}
